//! Bitmap glyph fonts for the software rendering backend.
//!
//! A [`BitmapFont`] holds a static 1-bit-per-pixel glyph table.  Each glyph
//! is stored as `glyph_height` bytes, one byte per row, MSB = leftmost pixel.
//! For the standard 8-pixel-wide VGA format one byte covers all 8 pixels of a
//! row; wider fonts would need two bytes per row, but that is not yet
//! supported.
//!
//! # Future work
//!
//! - **Expanded glyph cache:** For wider fonts (>8px), consider pre-computing
//!   expanded scanlines to avoid per-frame bit extraction. Currently the bit
//!   extraction loop is not a bottleneck for 8px-wide fonts at typical grid
//!   sizes, but wider fonts (10px, 16px) would benefit from caching.
//!   See ADR 012 (metrics) and ADR 011 (future optimization notes).
//!
//! - **Wider glyphs:** To support glyphs wider than 8px, change `rows()` to
//!   return `ceil(glyph_width / 8)` bytes per row and update the bit
//!   extraction in `blit_cell`/`blit_glyph` to index across bytes. PNG
//!   sprite tilesets via `SpriteCache` already support arbitrary tile sizes.

// ── BitmapFont ─────────────────────────────────────────────────────────────

/// A 1-bit-per-pixel bitmap glyph font.
///
/// `Copy` because it is just a static reference plus three small integers.
#[derive(Debug, Clone, Copy)]
pub struct BitmapFont {
    /// Glyph bitmap data: `glyph_count * glyph_height` bytes.
    data: &'static [u8],
    /// Width of each glyph in pixels (≤ 8 for single-byte rows).
    pub glyph_width: u8,
    /// Height of each glyph in pixels; also bytes per glyph.
    pub glyph_height: u8,
    /// Total number of glyphs stored in `data`.
    glyph_count: u16,
}

impl BitmapFont {
    /// Constructs a bitmap font from a static byte slice.
    ///
    /// `data` must contain exactly `glyph_count * glyph_height` bytes.
    #[must_use]
    pub const fn new(
        data: &'static [u8],
        glyph_width: u8,
        glyph_height: u8,
        glyph_count: u16,
    ) -> Self {
        Self {
            data,
            glyph_width,
            glyph_height,
            glyph_count,
        }
    }

    /// Returns the row bytes for glyph `index`.
    ///
    /// Each byte is one row; bit 7 (MSB) is the leftmost pixel.
    ///
    /// # Panics
    ///
    /// Panics if `index as u16 >= self.glyph_count`.
    #[must_use]
    pub fn rows(&self, index: u8) -> &[u8] {
        assert!(
            u16::from(index) < self.glyph_count,
            "glyph index {index} out of range ({})",
            self.glyph_count,
        );
        let h = usize::from(self.glyph_height);
        let start = usize::from(index) * h;
        &self.data[start..start + h]
    }

    /// Maps a Unicode `char` to a glyph index in this font using the CP437
    /// encoding, falling back to a solid-block glyph for unmapped characters.
    #[must_use]
    pub const fn char_to_index(&self, ch: char) -> u8 {
        unicode_to_cp437(ch)
    }
}

// Two `BitmapFont`s are equal when they point at the same static data and
// share the same dimensions.  Comparing the full 4 KB slice on every draw
// call would be wasteful, so we compare the data pointer instead.
impl PartialEq for BitmapFont {
    fn eq(&self, other: &Self) -> bool {
        core::ptr::eq(self.data.as_ptr(), other.data.as_ptr())
            && self.glyph_width == other.glyph_width
            && self.glyph_height == other.glyph_height
            && self.glyph_count == other.glyph_count
    }
}

impl Eq for BitmapFont {}

