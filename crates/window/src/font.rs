//! Bitmap glyph fonts and CP437 mapping, shared by retroglyph's graphical backends.
//!
//! A [`BitmapFont`] holds a static 1-bit-per-pixel glyph table. Each glyph is stored as
//! `glyph_height` bytes, one byte per row, MSB = leftmost pixel. For the standard 8-pixel-wide
//! VGA format one byte covers all 8 pixels of a row; wider fonts would need two bytes per row,
//! but that is not yet supported.
//!
//! This module is the dependency-free glyph-source layer both `retroglyph-software` (CPU
//! rasterizer) and `retroglyph-gl` (GPU atlas) build on, so their text output stays
//! pixel-identical. It lives here (rather than in a standalone crate) because both consumers
//! already depend on `retroglyph-window` for [`Presenter`](crate::Presenter), and it needs none of
//! winit -- it is available with `default-features = false`. Enable the `default-font` feature for
//! the embedded Unscii 16 font ([`unscii16::FONT`]); leave it off to supply your own via
//! [`BitmapFont::new`].
//!
//! # Future work
//!
//! - **Expanded glyph cache:** For wider fonts (>8px), consider pre-computing expanded scanlines
//!   to avoid per-frame bit extraction. Currently the bit extraction loop is not a bottleneck
//!   for 8px-wide fonts at typical grid sizes, but wider fonts (10px, 16px) would benefit from
//!   caching.
//!
//! - **Wider glyphs:** To support glyphs wider than 8px, change [`BitmapFont::rows`] to return
//!   `ceil(glyph_width / 8)` bytes per row and update the consumers' bit extraction to index
//!   across bytes. Tracked in retroglyph issue #164; deferred until a second, non-8px-wide font
//!   is actually needed.

// ── BitmapFont ─────────────────────────────────────────────────────────────

/// A 1-bit-per-pixel bitmap glyph font.
///
/// `Copy` because it is just a static reference plus three small integers.
#[derive(Debug, Clone, Copy)]
pub struct BitmapFont {
    /// Glyph bitmap data: `glyph_count * glyph_height` bytes.
    data: &'static [u8],
    /// Width of each glyph in pixels (≤ 8 for single-byte rows).
    pub glyph_width: u8,
    /// Height of each glyph in pixels; also bytes per glyph.
    pub glyph_height: u8,
    /// Total number of glyphs stored in `data`.
    glyph_count: u16,
}

impl BitmapFont {
    /// Constructs a bitmap font from a static byte slice.
    ///
    /// `data` must contain exactly `glyph_count * glyph_height` bytes.
    #[must_use]
    pub const fn new(
        data: &'static [u8],
        glyph_width: u8,
        glyph_height: u8,
        glyph_count: u16,
    ) -> Self {
        Self {
            data,
            glyph_width,
            glyph_height,
            glyph_count,
        }
    }

    /// Returns the row bytes for glyph `index`.
    ///
    /// Each byte is one row; bit 7 (MSB) is the leftmost pixel.
    ///
    /// # Panics
    ///
    /// Panics if `index as u16 >= self.glyph_count`.
    #[must_use]
    pub fn rows(&self, index: u8) -> &[u8] {
        assert!(
            u16::from(index) < self.glyph_count,
            "glyph index {index} out of range ({})",
            self.glyph_count,
        );
        let h = usize::from(self.glyph_height);
        let start = usize::from(index) * h;
        &self.data[start..start + h]
    }

    /// The total number of glyphs stored in this font.
    ///
    /// Glyph indices `0..glyph_count()` are valid arguments to [`rows`](Self::rows). A GPU
    /// backend uses this to size its glyph atlas (one texture-array layer per glyph).
    #[must_use]
    pub const fn glyph_count(&self) -> u16 {
        self.glyph_count
    }

    /// Maps a Unicode `char` to a glyph index in this font using the CP437
    /// encoding, falling back to a solid-block glyph for unmapped characters.
    #[must_use]
    pub const fn char_to_index(&self, ch: char) -> u8 {
        unicode_to_cp437(ch)
    }

    /// Attempts to map a Unicode `char` to a glyph index in this font, returning `None`
    /// instead of substituting the solid-block fallback glyph on a miss.
    ///
    /// A miss is either `ch` not being in the CP437 mapping table at all, or its mapped
    /// index falling outside this font's `glyph_count` (e.g. a font built with fewer than
    /// 256 glyphs). [`FallbackFontChain::resolve`] uses this to keep trying further fonts
    /// in the chain rather than settling for [`char_to_index`](Self::char_to_index)'s
    /// unconditional solid-block substitution.
    #[must_use]
    pub const fn try_char_to_index(&self, ch: char) -> Option<u8> {
        match try_unicode_to_cp437(ch) {
            Some(index) if (index as u16) < self.glyph_count => Some(index),
            _ => None,
        }
    }
}

// Two `BitmapFont`s are equal when they point at the same static data and
// share the same dimensions.  Comparing the full 4 KB slice on every draw
// call would be wasteful, so we compare the data pointer instead.
impl PartialEq for BitmapFont {
    fn eq(&self, other: &Self) -> bool {
        core::ptr::eq(self.data.as_ptr(), other.data.as_ptr())
            && self.glyph_width == other.glyph_width
            && self.glyph_height == other.glyph_height
            && self.glyph_count == other.glyph_count
    }
}

impl Eq for BitmapFont {}

// ── Fallback font chain ─────────────────────────────────────────────────────

/// A glyph resolved from a [`FallbackFontChain`]: the glyph index plus the specific
/// [`BitmapFont`] it came from, since each font in a chain owns its own bitmap data.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct ResolvedGlyph {
    font: BitmapFont,
    index: u8,
}

impl ResolvedGlyph {
    /// The font this glyph was resolved from.
    #[must_use]
    pub const fn font(&self) -> &BitmapFont {
        &self.font
    }

    /// The glyph index within [`font`](Self::font).
    #[must_use]
    pub const fn index(&self) -> u8 {
        self.index
    }

    /// Returns the row bytes for this glyph; see [`BitmapFont::rows`].
    #[must_use]
    pub fn rows(&self) -> &[u8] {
        self.font.rows(self.index)
    }
}

/// Combines a primary [`BitmapFont`] with an ordered list of fallback fonts.
///
/// [`resolve`](Self::resolve) tries the primary font's char mapping first; on a miss, it
/// tries each fallback font in order; only if every font in the chain misses does it fall
/// back to the primary font's solid-block glyph. This lets a caller layer, say, an ASCII
/// or partial-coverage primary font with one or more broader fallback fonts, so a char
/// missing from the primary doesn't automatically become a solid block if some other font
/// in the chain actually has it.
///
/// This type ships **no bundled fallback font data** -- every font in the chain, primary
/// or fallback, is supplied by the caller. Bundling a ready-to-use Latin-1/Extended
/// fallback font is a natural follow-up now that this mechanism exists, but is out of
/// scope here.
///
/// Callers who don't need a fallback chain can keep using a bare [`BitmapFont`] and
/// [`BitmapFont::char_to_index`]/[`BitmapFont::rows`] directly, exactly as before --
/// this type is purely additive and opt-in.
#[derive(Debug, Clone, Copy)]
pub struct FallbackFontChain<'a> {
    primary: BitmapFont,
    fallbacks: &'a [BitmapFont],
}

impl<'a> FallbackFontChain<'a> {
    /// Constructs a chain from a primary font and an ordered list of fallback fonts.
    #[must_use]
    pub const fn new(primary: BitmapFont, fallbacks: &'a [BitmapFont]) -> Self {
        Self { primary, fallbacks }
    }

    /// Resolves `ch` to a glyph, trying the primary font first, then each fallback font
    /// in order, only substituting the primary font's solid-block glyph if every font in
    /// the chain misses.
    #[must_use]
    pub fn resolve(&self, ch: char) -> ResolvedGlyph {
        if let Some(index) = self.primary.try_char_to_index(ch) {
            return ResolvedGlyph {
                font: self.primary,
                index,
            };
        }
        for font in self.fallbacks {
            if let Some(index) = font.try_char_to_index(ch) {
                return ResolvedGlyph { font: *font, index };
            }
        }
        ResolvedGlyph {
            font: self.primary,
            index: FALLBACK,
        }
    }
}

